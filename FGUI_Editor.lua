                --  发布：默认功能是发布当前的活跃包（如果编辑器焦点在资源库，则活跃包是指资源库当前选中的资源所在的包；如果编辑器焦点在文档视图，则活跃包是指当前正在编辑的组件所在的包）。这个功能可以通过偏好设置的选项改变为发布所有的包或者发布所有修改但未发布的包。
                --  仅发布定义：如果用户没有修改图片，动画，字体等素材，而仅仅修改了界面布局之类，可以只发布定义，也就是不重新生成纹理集，这样可以提升发布速度，但如果包的内容不是非常多，这个提升不显著。
